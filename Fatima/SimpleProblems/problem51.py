print("Enter your grade:")
letter = input()

letter = letter.lower()

if letter == 'a+' :
    print("You got 4 points.")
elif letter == 'a' :
    print("You got 4 points.")
elif letter == 'a-' :
    print("You got 3.7 points.")
elif letter == 'b+' :
    print("You got 3.3 points.")
elif letter == 'b' :
    print("You got 3 points.")
elif letter == 'b-' :
    print("You got 2.7 points.")
elif letter == 'c+' :
    print("You got 2.3 points.")
elif letter == 'c' :
    print("You got 2 points.")
elif letter == 'c-' :
    print("You got 1.7 points.")
elif letter == 'd+' :
    print("You got 1.3 points.")
elif letter == 'd' :
    print("You got 1 points.")
elif letter == 'f' :
    print("You got 0 points.")
else:
    print("Invalid input - enter a proper grade.")
