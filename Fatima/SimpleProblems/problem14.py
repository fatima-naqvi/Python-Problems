PI = 3.14

print("Enter the radius: ")
radius: float = float(input())

areaOfACircle: float = float(PI * radius * radius)
print("The are of your circle is: %d.03" %areaOfACircle)

volumeOfASphere: float = float((3/4) * PI * radius * radius * radius)
print("The volume of your sphere is: %d.03" %volumeOfASphere)

